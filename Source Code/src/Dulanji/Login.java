    private void updateChart() {
        DefaultCategoryDataset dset = new DefaultCategoryDataset();

        Set keys = dataHashMap.keySet();
        Iterator prodInforItr = keys.iterator();

        while (prodInforItr.hasNext()) {

            String iname = (String) prodInforItr.next();
            float iquanty = (float) dataHashMap.get(iname);
            dset.setValue(iquanty, "Stock", iname);

        }
        JFreeChart jchart = ChartFactory.createBarChart("Stock Report", "Item Name", "Quantity", dset, PlotOrientation.VERTICAL, true, true, false);

        ChartPanel charpanl = new ChartPanel(jchart);
        chartgit.removeAll();
        chartgit.add(charpanl);
        chartgit.updateUI();

    }

