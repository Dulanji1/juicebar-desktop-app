    private void loadData() {
        try {
            DefaultTableModel tableModel = (DefaultTableModel) supplierdetailstabel.getModel();
            String qry = "SELECT * FROM supplier_details";
            ResultSet result = Dbconection.search(qry);
            tableModel.setRowCount(0);
            while (result.next()) {
                Vector v = new Vector();
                v.add(result.getString("supplier_details.Supplier_No"));
                v.add(result.getString("supplier_details.Supplier_Name"));
                v.add(result.getString("supplier_details.Supplier_Address"));
                v.add(result.getString("supplier_details.Supplier_Phone_No"));
                v.add(result.getString("supplier_details.Supplier_Email"));
                tableModel.addRow(v);
            }

            for (int i = 0; i < supplierdetailstabel.getColumnCount(); i++) {
                DefaultTableCellRenderer renderer = new DefaultTableCellRenderer();
                supplierdetailstabel.getColumnModel().getColumn(i).setCellRenderer(renderer);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

