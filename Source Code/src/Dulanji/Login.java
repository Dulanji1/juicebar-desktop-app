    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed

        try {
            String qry = "SELECT * FROM  login";
            ResultSet result = Dbconection.search(qry);
            int i = 0;
            while (result.next()) {
                if (jTextField1.getText().equalsIgnoreCase(result.getString("username"))) {
                    if (String.valueOf(jPasswordField1.getPassword()).equals(result.getString("password"))) {
                        JOptionPane.showMessageDialog(this, "  Access Granted\n\n  Welcome User : \n" + jTextField1.getText().toUpperCase());
                        Main_CartLayout.removeAll();
                        Main_CartLayout.add(Home);
                        Main_CartLayout.repaint();
                        Main_CartLayout.revalidate();

                        i++;
                        y++;

                        jTextField1.setText("");
                        jPasswordField1.setText("");

                    }
                }
            }

            if (i == 0) {
                JOptionPane.showMessageDialog(this, "No such user", "Error Login", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton8ActionPerformed

